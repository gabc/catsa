CREATE OR REPLACE TRIGGER CS_News_TRG
BEFORE INSERT ON CS_News
FOR EACH ROW
BEGIN
  IF :new.ID IS NULL THEN
    SELECT News_SEQ.nextval INTO :new.ID FROM DUAL;
  END IF;
END;
/

