CREATE OR REPLACE TRIGGER CS_Carte_Before_trg
BEFORE INSERT OR UPDATE ON CS_NEWS
FOR EACH ROW
BEGIN
  IF INSERTING THEN
    :new.created := sysdate;
  END IF;
  
  :new.lastModified := sysdate;
END;
